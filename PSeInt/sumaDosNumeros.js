//realizar la suma de ambos y mostrar el resultado.

let num1 = parseInt(prompt("Ingrese el primer número"));
let num2 = parseInt(prompt("Ingrese el segundo número"));

let suma = num1 + num2;

document.writeln("La suma de los dos números es: " + suma);